package dto;

public class ImagesBean {
	
	private int img_idx;
	private int img_post_idx;
	private String img_path;
	private String img_file_name;
	private String img_regdate;
	
	
	public int getImg_idx() {
		return img_idx;
	}
	public void setImg_idx(int img_idx) {
		this.img_idx = img_idx;
	}
	public int getImg_post_idx() {
		return img_post_idx;
	}
	public void setImg_post_idx(int img_post_idx) {
		this.img_post_idx = img_post_idx;
	}
	public String getImg_path() {
		return img_path;
	}
	public void setImg_path(String img_path) {
		this.img_path = img_path;
	}
	public String getImg_file_name() {
		return img_file_name;
	}
	public void setImg_file_name(String img_file_name) {
		this.img_file_name = img_file_name;
	}
	public String getImg_regdate() {
		return img_regdate;
	}
	public void setImg_regdate(String img_regdate) {
		this.img_regdate = img_regdate;
	}
	
	

}
